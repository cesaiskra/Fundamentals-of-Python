'''
An object's momentum is its mass multiplied by its velocity. Write a pro-
gram that accepts an object's mass (in kilograms) and velocity (in meters
per second) as inputs and then outputs its momentum.
'''

#In physics, the symbol for the quantity momentum is the lower case p. Thus, the above equation can be rewritten as
#p = m*v


